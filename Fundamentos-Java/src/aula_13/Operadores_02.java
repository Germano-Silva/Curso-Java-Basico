package aula_13;

public class Operadores_02 {
	public static void main(String[] args) {
		int v1 = 1;
		int v2 = 2;
		
		System.out.println("valor1 == valor2: " + (v1 == v2));
		System.out.println("valor1 != valor2: " + (v1 != v2));
		System.out.println("valor1 >  valor2: " + (v1 >  v2));
		System.out.println("valor1 >= valor2: " + (v1 >= v2));
		System.out.println("valor1 <  valor2: " + (v1 <  v2));
		System.out.println("valor1 <= valor2: " + (v1 <= v2));
	}

}
