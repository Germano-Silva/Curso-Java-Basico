package aula_11;

public class TiposPrimitivos_05 {

	public static void main(String[] args) {
		
		int var1 = 2147483647;
		int var2 = 100;

		System.out.println(var1 + var2);

		// toda vez que ultrapassar o valor max de uma varivel em java decorrente da
		// somatoria de duas variaveis o resultado vais ser o valor minimo do tipo da
		// variavel, pois os numeros em java se comportam como uma roleta.

	}

}
